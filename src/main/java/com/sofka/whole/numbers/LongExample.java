package com.sofka.whole.numbers;

public class LongExample {
    public static void main(String[] args) {
        long minValue = -9223372036854775808L; // Minimum long value (ends with 'L' to indicate it's a long)
        long maxValue = 9223372036854775807L;  // Maximum long value (also ends with 'L')

        System.out.println("Minimum long value: " + minValue);
        System.out.println("Maximum long value: " + maxValue);
    }
}
